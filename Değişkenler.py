# type komutu ile değişkenin türünü öğrenebiliriz
print(type(507)) # int tam sayı değişkenini

print(type(5.07)) # float ondalıklı sayıyı

print(type("Tayfun")) # str yazı karakterini

print(type([5.07])) # list liste olduğunu

print(type({"Tayfun":"Yakut"})) # dict ise değişkenin sözlük türünde olduğunu gösterir


